#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int n;

	i = 0;
	n = 0;
	if ((str[0] == '+' || str[0] == '-') && ft_isdigit(str[1]) != 0)
		i++;
	if (str[i] == '0')
		return (0);
	while (ft_isdigit(str[i]) != 0)
	{
		n *= 10;
		n += (str[i] - 48);
		i++;
	}
	n *= (str[0] == '-') ? -1 : 1;
	return (n);
}
